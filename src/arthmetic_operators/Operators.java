package arthmetic_operators;

public class Operators {
    public static void main(String[] args) {
        int a = 8;
        int b = 6;
        System.out.println("a + b = " + (a + b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a % b = " + (a % b));
    }
}